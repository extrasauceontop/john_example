# clones repo for the first time to your machine
'git clone <repo link here>'

# checks the status of files in a repo
'git status'

# adds files to git tracking
'git add <file name> or git add . to track everything at once'

# commits changes
'git commit -m <message here>'

# pushes up to the remote repo
'git push'

# create a new branch
'git checkout -b <branch name>'